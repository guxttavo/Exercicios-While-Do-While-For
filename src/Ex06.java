/*
-->Faça um programa que imprima na tela os números de 1 a 20, um
   abaixo do outro.
 */

import java.util.Scanner;

public class Ex06 {

    public static void main(String[] args) {

        int num = 20;

        for (int i = 0; i <= num; i++) {
            System.out.println(i);
        }
    }
}
